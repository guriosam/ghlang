package utils;

public class CLIConfig {

	public final String pathCode;
	public final String pathTokens;
	public final String pathOut;
	
	/**
	 * CLIConfig constructor.
	 *
	 * @param pathCode
	 * @param pathTokens
	 * @param pathOut 
	 */
	public CLIConfig(String pathCode, String pathTokens, String pathOut) {
		this.pathCode = pathCode;
		this.pathTokens = pathTokens;
		this.pathOut = pathOut;
	}



	
	
}
